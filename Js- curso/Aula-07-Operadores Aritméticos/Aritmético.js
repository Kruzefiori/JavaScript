/**
 * Aritméticos 
 * + Adição & Concatenação 
 * 
 * - * / Decremento multiplicação e divisão         ++ & -- Funciona como em C porém há o pré e o pós    ++x (soma primeiro e mostra depois)
 *  ** potenciação                                                                                       x++ (mostra primeiro e soma depois)
 * % retorna o resto da divisão
 */

const nu1 = 2
const nu2 = 5

console.log(nu1 + nu2) //soma os numeros

const nu3 = "20" //string

console.log(nu1 + nu3)//concatena

console.log(nu1 ** nu2)

console.log(nu2 % nu1)


